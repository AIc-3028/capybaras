package com.company;

public class Naming {

    // camelCase
    // first word is lowercase
    // every word after is capitalized
    // this one is used for every variable
    int myInt = 3;

    // PascalCase
    // first word is capitalized
    // every word after is capitalized as well
    // classes are typically PascalCase
    int OtherInt = 4;

    // snake_case
    // every word is separated with an underscore

    int this_is_my_int = 5;

    // hungarian notation
    int iMyInt = 5;

    // ALL_CAPS
    // this is used to denote constants
    double TAX_RATE = 7.0;


}
